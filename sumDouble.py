# Description: function that returns the sum of two different #s and shows double the sum if they are the same

def sumdouble(a,b):
      if a == b:
            print('the doubled sum is', (a+b)*2)
      else:
            return a + b
print(sumdouble(1,2))
print(sumdouble(3,4))
sumdouble(2,2)
